export const searchPlants = (searchTerm) => {
  const allPlants = [
    ...LAYER_1_ALL,
    ...LAYER_2_ALL,
    ...LAYER_3_ALL,
    ...LAYER_4_PLANTS,
    ...LAYER_5_PLANTS,
    ...LAYER_6_PLANTS,
    ...LAYER_7_PLANTS,
  ];
  const term = searchTerm.toLowerCase();
  return allPlants.filter(
    (plant) =>
      plant.name.toLowerCase().includes(term) ||
      plant.hindi_name.includes(term) ||
      plant.scientific_name.toLowerCase().includes(term) ||
      plant.benefits.some((benefit) => benefit.toLowerCase().includes(term))
  );
};
